var keyLeft = false, keyUp = false, keyRight = false, keyDown = false;

function arrowKeyDown(e) {
    var key = e.keyCode;
    if (key == 37) {
        keyLeft = true;
    } else if (key == 38) {
        keyUp = true;
    } else if (key == 39) {
        keyRight = true;
    } else if (key == 40) {
        keyDown = true;
    }
}

function arrowKeyUp(e) {
    var key = e.keyCode;
    if (key == 37) {
        keyLeft = false;
    } else if (key == 38) {
        keyUp = false;
    } else if (key == 39) {
        keyRight = false;
    } else if (key == 40) {
        keyDown = false;
    }
}

