var canvasArea = {
    canvas : document.getElementById("canvasArea"),
    start : function() {
        this.canvas.width = 600;
        this.canvas.height = 600;
        this.context = this.canvas.getContext("2d");
        this.interval = setInterval(gameTick, 20);
    },
    clear : function() {
        this.context.clearRect(0, 0, this.canvas.width, this.canvas.height);
    }
}

var camera = {
    xAnchor : 0,
    yAnchor : 0,
    xReach : 600,
    yReach : 600,
    update : function() {
        if (keyDown && this.yReach < currentMap.y * 50 && player.y == (camera.yReach - camera.yAnchor) / 2 - 20) {
            this.yAnchor += 4;
            this.yReach += 4;
            player.y = (camera.yReach - camera.yAnchor) / 2 - 20;
        } else if (keyDown && player.y + player.height < currentMap.y * 50) {
            player.y += 4;
            if (player.y > (camera.yReach - camera.yAnchor) / 2 - 20 && this.yReach < currentMap.y * 50) {
                player.y == (camera.yReach - camera.yAnchor) / 2 - 20;
            }
        }
        if (keyUp && this.yAnchor > 0 && player.y == (camera.yReach - camera.yAnchor) / 2 - 20) {
            this.yAnchor += -4;
            this.yReach += -4;
        } else if (keyUp && player.y > 0) {
            player.y += -4;
            if (player.y < (camera.yReach - camera.yAnchor) / 2 - 20 && this.yAnchor > 0) {
                player.y == (camera.yReach - camera.yAnchor) / 2 - 20;
            }
        }
        if (keyRight && this.xReach < currentMap.x * 50 && player.x == (camera.xReach - camera.xAnchor) / 2 - 20) {
            this.xAnchor += 4;
            this.xReach += 4;
        } else if (keyRight && player.x + player.width < currentMap.x * 50) {
            player.x += 4;
            if (player.x > (camera.xReach - camera.xAnchor) / 2 - 20 && this.xReach < currentMap.x * 50) {
                player.x == (camera.xReach - camera.xAnchor) / 2 - 20;
            }
        }
        if (keyLeft && this.xAnchor > 0 && player.x == (camera.xReach - camera.xAnchor) / 2 - 20) {
            this.xAnchor += -4;
            this.xReach += -4;
        } else if (keyLeft && player.x > 0) {
            player.x += -4;
            if (player.x < (camera.xReach - camera.xAnchor) / 2 - 20 && this.xAnchor > 0) {
                player.x == (camera.xReach - camera.xAnchor) / 2 - 20;
            }
        }
    }
}

var player = {
    width : 40,
    height : 40,
    x : (camera.xReach - camera.xAnchor) / 2 - 20,
    y : (camera.yReach - camera.yAnchor) / 2 - 20,
    update : function() {
        ctx = canvasArea.context;
        ctx.fillStyle = "rgb(255,0,0)";
        ctx.fillRect(this.x,this.y,this.width,this.height);
    }
}

function tile(x,y,id) {
    this.width = 50;
    this.height = 50;
    this.x = x * 50 - camera.xAnchor;
    this.y = y * 50 - camera.yAnchor;
    this.id = id;
    this.update = function() {
        this.x = x * 50 - camera.xAnchor;
        this.y = y * 50 - camera.yAnchor;
        ctx = canvasArea.context;
        ctx.fillStyle = tileId[this.id].color;
        ctx.fillRect(this.x,this.y,this.width,this.height);
    }
}

function tileMap(map,x,y) {
    this.map = map;
    this.tiles = [];
    this.x = x;
    this.y = y;
    this.start = function() {
        var y, x;
        for (y=0; y < this.y; y++) {
            for (x=0; x < this.x; x++) {
                this.tiles.push(new tile(x,y,this.map[(y*this.x)+x]));
            }
        }
    }
    this.update = function() {
        var i;
        for (i=0; i < this.tiles.length; i++) {
            this.tiles[i].update();
        }
    }
}

function gameTick() {
    canvasArea.clear();
    currentMap.update();
    camera.update();
    player.update();
}

